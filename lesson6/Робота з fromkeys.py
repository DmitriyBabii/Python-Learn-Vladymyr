"""
При допомозі методу fromkeys створіть словник, який буде складатися з трьох елементів. Ключами будуть:

"Математика", "Англійська" і "Наука". Значення для всіх буде число 0.

Виконайте завдання:

виведіть весь словник у консоль;
виведіть кожен елемент окремо через цикл for;
відсортуйте словник за його ключами.
"""

data = {}.fromkeys(["Математика", "Англійська", "Наука"], 0)
print(data)
for key, values in data.items():
    print(key, values, sep="-")
print(sorted(data))
