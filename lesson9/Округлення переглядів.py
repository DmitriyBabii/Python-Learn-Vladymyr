"""
Створіть функцію, яка буде округлювати чотирицифрові числа, замінюючи всі цифри,
 що стоять праворуч від розряду числа, буквою «К». Число менше тисячі буде виводитися без змін.

# Передаємо число 1272
# Отримуємо - 1.2К
"""


def about(num):
    if num < 1000:
        print(num)
    else:
        num = round(num/1000, 1)
        print(f"{num}K")

num = int(input("Введіть число: "))
about(num)