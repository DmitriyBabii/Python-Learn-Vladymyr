"""
Створіть функцію ділення чисел, яка прийматиме три параметри. Зробіть останній параметр з значенням
 за замовчуванням.

Викличте функцію два рази:

з передачею третього параметра
без передачі третього параметра
"""


def delenie(a, b, c=2):
    if b != 0 and c != 0:
        return a / b / c
    else:
        print("Ділення на нуль!")

a = int(input("Введіть число a: "))
b = int(input("Введіть число b: "))
c = int(input("Введіть число c: "))
print(delenie(a, b))
print(delenie(a, b, c))